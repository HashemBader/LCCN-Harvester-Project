# --- Helpers ---

def get_icon(svg_data: str, color: str = "#a5adcb") -> QIcon:
    """
    Renders an SVG string into a QIcon with the specified color.
    """
    # 1. Modify SVG stroke color (naive string replacement for simplicity)
    # Ensure usage of 'stroke="currentColor"' in SVGs allows this if we set logical color,
    # but strictly setting it via string replacement is robust for simple icon sets.
    colored_svg = svg_data.replace("currentColor", color)
    
    # 2. Render to QPixmap
    renderer = QSvgRenderer(QByteArray(colored_svg.encode('utf-8')))
    pixmap = QPixmap(24, 24)
    pixmap.fill(Qt.GlobalColor.transparent)
    
    painter = QPainter(pixmap)
    renderer.render(painter)
    painter.end()
    
    return QIcon(pixmap)

def get_pixmap(svg_data: str, color: str = "#a5adcb", size: int = 24) -> QPixmap:
    colored_svg = svg_data.replace("currentColor", color)
    renderer = QSvgRenderer(QByteArray(colored_svg.encode('utf-8')))
    pixmap = QPixmap(size, size)
    pixmap.fill(Qt.GlobalColor.transparent)
    painter = QPainter(pixmap)
    renderer.render(painter)
    painter.end()
    return pixmap
